// Примітивні типи даних
let number = 10; // Ціле число (number)
let floatingNumber = 3.14; // Дійсне число (number)
let string = "Hello, world!"; // Рядок (string)
let boolean = true; // Булеве значення (boolean)
let nullValue = null; // Значення null (object)
let undefinedValue; // Значення undefined (undefined)

console.log("Примітивні типи даних:");
console.log("Ціле число:", number);
console.log("Дійсне число:", floatingNumber);
console.log("Рядок:", string);
console.log("Булеве значення:", boolean);
console.log("Значення null:", nullValue);
console.log("Значення undefined:", undefinedValue);

// Силочні типи даних
let array = [1, 2, 3]; // Масив (object)
let object = { key: "value" }; // Об'єкт (object)
let functionType = function () {}; // Функція (function)

console.log("\nСилочні типи даних:");
console.log("Масив:", array);
console.log("Об'єкт:", object);
console.log("Функція:", functionType);
