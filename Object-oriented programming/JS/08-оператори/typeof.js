// Унарний оператор
typeof 10;
typeof "Ihor";
typeof false;
console.log(typeof 10); // Виведе "number" у консоль
console.log(typeof "Ihor"); // Виведе "string" у консоль
console.log(typeof false); // Виведе "boolean" у консоль

typeof 10 === "number";
console.log(typeof 10 === "number"); // Виведе "true" у консоль

let isUndefined;
typeof isUndefined === "undefined";
console.log(isUndefined);
