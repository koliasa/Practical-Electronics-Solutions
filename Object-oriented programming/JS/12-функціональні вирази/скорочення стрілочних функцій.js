const sum = (a, b) => a + 1 + b;

// Викликаємо функцію та виводимо результат у консоль
console.log(sum(5, 10)); // Очікуваний результат: 16

// В даному прикладі функція sum стає ще коротшою, оскільки ми використали скорочений синтаксис, у якому вираз в тілі функції повертається без ключового слова return.
