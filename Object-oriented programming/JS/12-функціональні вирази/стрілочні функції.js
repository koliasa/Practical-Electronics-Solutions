// Приймає два параметри a і b.
// Збільшує значення a на 1.
// Призначає змінній c значення, яке є сумою значень a та b.
// Повертає значення c.

const sum = (a, b) => {
  let c;
  a = a + 1;
  c = a + b;
  return c;
};

// Викликаємо функцію та виводимо результат у консоль
console.log(sum(5, 10)); // Очікуваний результат: 16
