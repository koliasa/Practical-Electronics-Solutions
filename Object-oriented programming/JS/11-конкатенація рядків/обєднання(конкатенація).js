// Конкатенація рядків - це процес об'єднання двох або більше рядків в одну. У JavaScript для конкатенації використовується оператор +.
// Приклади конкатенації рядків:

// Приклад 1: Конкатенація змінних і рядків
let firstName = "John";
let lastName = "Doe";

let fullName = firstName + " " + lastName;
console.log(fullName); // Виведе: "John Doe"

// Приклад 2: Конкатенація рядків і літералів
let greeting = "Hello";
let punctuation = "!";

let message = greeting + ", world" + punctuation;
console.log(message); // Виведе: "Hello, world!"

// Приклад 3: Конкатенація рядків і чисел (автоматичне перетворення чисел у рядки)
let num1 = 10;
let num2 = 20;

let result = "Результат додавання: " + (num1 + num2);
console.log(result); // Виведе: "Результат додавання: 30"

// У цих прикладах оператор + використовується для об'єднання рядків разом зі змінними, літералами та навіть числами.
