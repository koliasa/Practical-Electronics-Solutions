Для того, щоб встановити та налаштувати sudo для користувача dev на FreeBSD 14.0 i386, виконайте наступні кроки:

Встановіть пакет sudo, введіть наступну команду в терміналі:
sudo pkg install sudo
Якщо ви не маєте прав адміністратора, попросіть адміністратора встановити sudo для вас.

Додайте користувача dev до файлу конфігурації sudoers. Щоб це зробити, виконайте наступну команду в терміналі:
sudo visudo
Ця команда відкриє файл /usr/local/etc/sudoers у вашому улюбленому текстовому редакторі.

Додайте наступний рядок в кінці файлу:
dev ALL=(ALL) ALL
Збережіть файл та закрийте текстовий редактор.

Перевірте, чи sudo працює для користувача dev. Для цього виконайте наступну команду:
sudo whoami
Вас буде запитано про ваш пароль. Після того, як ви його введете, на екрані з'явиться результат команди whoami, який повинен бути root.

Тепер користувач dev може виконувати команди з підвищеними правами за допомогою sudo. Наприклад, він може встановлювати програми, оновлювати систему, тощо.\

Змінити права доступу до файлу sudoers. Введіть наступну команду в терміналі:
sudo chmod 644 /usr/local/etc/sudoers