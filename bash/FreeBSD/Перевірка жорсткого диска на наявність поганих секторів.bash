Показати всі жосткі диски
sudo camcontrol devlist
Запустити перевірку жорсткого диска з використанням badblocks, передавши йому ідентифікатор жорсткого диска:
sudo badblocks -b 512 /dev/ada0 > badsectors.txt
Після завершення перевірки, виконати команду для заблокування поганих секторів
sudo e2fsck -l badsectors.txt /dev/ada0

Щоб перевірити жорсткий диск та виправити помилки на FreeBSD
sudo fsck -y /dev/ada0

Для запуску fsck з опцією блокування поганих секторів вам потрібно вказати опцію -c при запуску команди.
sudo fsck -yc /dev/ada0
Ця команда виконає сканування файлової системи на пристрої /dev/ada0 з опцією блокування поганих секторів та автоматичним виправленням помилок. Будьте уважні при виконанні цієї операції, оскільки вона може зайняти деякий час і змінити дані на пристрої.

